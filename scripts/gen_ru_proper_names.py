def decline(name: str) -> tuple[str, str]:
    if name in OVERRIDE:
        return OVERRIDE[name]
    if "-" in name:
        return name, name
    if name.endswith("ия"):
        stem = name[:-2]
        return f"{stem}ии", f"{stem}ией"
    if name.endswith("а"):
        stem = name[:-1]
        return f"{stem}ы", f"{stem}ой"
    if name.endswith("я"):
        return f"{name[:-1]}и", f"{name[:-1]}ей"
    if name.endswith("ь"):
        return f"{name[:-1]}я", f"{name[:-1]}ем"
    if name.endswith("й"):
        return f"{name[:-1]}я", f"{name[:-1]}ем"
    if name.endswith("о"):
        return f"{name[:-1]}а", f"{name[:-1]}ом"
    if name.endswith("р") or name.endswith("н") or name.endswith("с"):
        return f"{name}а", f"{name}ом"
    return f"{name}а", f"{name}ом"


def emit_proper_names() -> str:
    lines = ["proper_names = ["]
    for name in NAMES:
        patron, verbal = decline(name)
        lines.append(
            f'    {{ name = "{name}", patron = "{patron}", verbal = "{verbal}" }},'
        )
    lines.append("]")
    return "\n".join(lines) + "\n"


def patch_vocab() -> None:
    text = VOCAB.read_text(encoding="utf-8")
    pattern = r"\[epithet\]\nproper_names = \[[\s\S]*?\]\n?"
    replacement = "[epithet]\n" + emit_proper_names()
    new_text, n = re.subn(pattern, replacement, text, count=1)
    if n != 1:
        raise SystemExit(f"proper_names block not found in {VOCAB} (replacements={n})")
    VOCAB.write_text(new_text, encoding="utf-8")
    print(f"Updated {VOCAB} ({len(NAMES)} names)")


if __name__ == "__main__":
    patch_vocab()
